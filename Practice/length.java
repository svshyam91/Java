class length {
	public static void main(String[] args) {
		int a1[] = new int[10];
		int a2[] = {10, 20, 30, 40, 50};
		int a3[] = {10, 20, 30, 40, 50, 60, 70, 80};

		System.out.println("The length of array a1 is "+a1.length);
		System.out.println("The length of array a2 is "+a2.length);
		System.out.println("The length of array a3 is "+a3.length);
	}
}